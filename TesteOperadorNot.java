public class TesteOperadorNot {
	
	public static void main (String[] args) {
		boolean ok = true;
		
		System.out.println ("Ok: " + ok);
		
		System.out.println ("Ok: " + !ok);
		
		System.out.println ("Ok: " + !!ok);
		
		System.out.println ("Ok: " + !!!ok);
		
		System.out.println ("Ok: " + !!!!ok);
		
		System.out.println ("Ok: " + !!!!!ok);
		
		System.out.println ("Ok: " + !!!!!!!!!!!!!!!!!!!!!!!!!!!!ok);
	}
}

