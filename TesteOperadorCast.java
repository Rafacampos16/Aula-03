public class TesteOperadorCast {
	
	public static void main (String[] args) {
		int x = 5 / 2;
		System.out.println ("5/2: " + x);
		
		double y = 5 /2;
		
		System.out.println ("y = 5/2: " + y);
		
		double z = (double) 5 / 2;
		
		System.out.println ("z = (double5/2: " + z);
	}
}

